    private String generateHTML(String title, String topic, String content, String imagePath) {
        // Calculate dynamic font sizes based on text length
        int titleFontSize = calculateFontSize(title, 40);
        int topicFontSize = calculateFontSize(topic, 30);
        int contentFontSize = calculateFontSize(content, 20);

        // Generate HTML
        String html = "<html>";

        // Add Image (if available)
        if (!imagePath.isEmpty()) {
            html += "<img src='file:///" + imagePath.replace("\\", "/") + "' width='1000'/><br>";
        }

        // Add Title
        html += "<span style='font-size:" + titleFontSize + "px;'><b>Title:</b> " + title + "</span><br>";

        // Add Topic
        html += "<span style='font-size:" + topicFontSize + "px;'><b>Topic:</b> " + topic + "</span><br>";

        // Add Content
        html += "<span style='font-size:" + contentFontSize + "px;'><b>Content:</b><br>" + content.replaceAll("\n", "<br>") + "</span>";

        html += "</html>";
        return html;
    }
        private int calculateFontSize(String text, int baseSize) {
        // Adjust font size based on text length
        int length = text.length();
        if (length <= 10) return baseSize;
        if (length <= 20) return baseSize - 5;
        if (length <= 30) return baseSize - 10;
        return baseSize - 15;
    } 
    
