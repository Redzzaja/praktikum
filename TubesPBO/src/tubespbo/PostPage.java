    public class RoundedBorder implements Border {
    private int radius; // Ukuran radius
    private Color borderColor; // Warna border
    private int thickness; // Ketebalan border

    public RoundedBorder(int radius, Color borderColor, int thickness) {
        this.radius = radius;
        this.borderColor = borderColor;
        this.thickness = thickness;
    }
    

    @Override
    public void paintBorder(Component c, Graphics g, int x, int y, int width, int height) {
        Graphics2D g2 = (Graphics2D) g;
        g2.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);
        g2.setColor(borderColor);
        g2.setStroke(new BasicStroke(thickness)); // Mengatur ketebalan garis
        g2.drawRoundRect(
            x + thickness / 2, 
            y + thickness / 2, 
            width - thickness, 
            height - thickness, 
            radius, 
            radius
        );
    }

    @Override
    public Insets getBorderInsets(Component c) {
        return new Insets(radius + thickness, radius + thickness, radius + thickness, radius + thickness);
    }

    @Override
    public boolean isBorderOpaque() {
        return false;
    }

}
